scenario = radarScenario('UpdateRate', 10);

radarPlatform = platform(scenario, 'Position', [0, 0, -20]);

radar = radarDataGenerator(1, ...
    'ScanMode', 'Mechanical', ...
    'MountingAngles', [0, 0, 0], ...
    'FieldOfView', [3, 10], ...
    'AzimuthResolution', 3, ...
    'RangeResolution', 75, ...
    'CenterFrequency', 3e9, ...
    'Bandwidth', 2e6, ...
    'MechanicalAzimuthLimits', [-45, 45], ...
    'MaxAzimuthScanRate', 30, ...
    'UpdateRate', 10, ...
    'ReferenceRange', 80e3, ...
    'ReferenceRCS', 0, ...
    'DetectionProbability', 0.9, ...
    'FalseAlarmRate', 1e-6, ...
    'RangeLimits', [0, 100e3], ...
    'DetectionCoordinates', 'Sensor rectangular');

radarPlatform.Sensors = radar;

rng(42);
nTargets = 100;
targetRanges = 50e3 + (98e3 - 50e3) * rand(nTargets, 1);
targetAzimuths = -40 + 80 * rand(nTargets, 1);
targetAlts = 100 + 900 * rand(nTargets, 1);

for i = 1:nTargets
    x = targetRanges(i) * cosd(targetAzimuths(i));
    y = targetRanges(i) * sind(targetAzimuths(i));
    z = -targetAlts(i);
    tgt = platform(scenario, 'Position', [x, y, z]);
    if i <= 50
        tgt.Signatures = rcsSignature('Pattern', 0, 'FluctuationModel', 'Swerling0');
    else
        tgt.Signatures = rcsSignature('Pattern', 0, 'FluctuationModel', 'Swerling1');
    end
end

scenario.StopTime = 30;

